console.log(1==1);
// 같으면 true, 다르면 false
console.log(1==2);
console.log(1>2);
console.log(1<2);
console.log(1>=2);
console.log(1<=2);
console.log(1!=2);
console.log(1!=1);
// 같지 않으면 true, 같으면 false
console.log(1==1 && 2==2); // true
console.log(1==1 && 2==3); // false
console.log(1==2 && 2==3); // false
console.log(1==1 || 2==2); // true
console.log(1==1 || 2==3); // true
